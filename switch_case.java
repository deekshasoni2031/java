public class switch_case {
    public static void main(String[] args) {
        int a=5;
        switch (a) {
            case 1:
            case 2:
            case 3:
            case 4:
            System.out.println("hello");
            break;
            case 5:
            System.out.println("hiiiii");
            break;
            default :

        }
    }
    
}
